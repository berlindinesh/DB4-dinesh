// Function to get cities for a given state
export const getCitiesForState = (state) => {
  if (!state || !indianCities[state]) {
    return [];
  }
  return Object.keys(indianCities[state]);
};

// Function to get districts for a given state and city
export const getDistrictsForCity = (state, city) => {
  if (!state || !city || !indianCities[state] || !indianCities[state][city]) {
    return [];
  }
  return indianCities[state][city].districts;
};

// Function to get pincodes for a given state and city
export const getPincodesForCity = (state, city) => {
  if (!state || !city || !indianCities[state] || !indianCities[state][city]) {
    return [];
  }
  return indianCities[state][city].pincodes;
};

// Function to validate if city belongs to the state
export const validateCityForState = (state, city) => {
  if (!state || !city) return false;
  const cities = getCitiesForState(state);
  return cities.includes(city);
};

// Function to validate if district belongs to the city and state
export const validateDistrictForCity = (state, city, district) => {
  if (!state || !city || !district) return false;
  const districts = getDistrictsForCity(state, city);
  return districts.includes(district);
};

// Function to validate if pincode belongs to the city and state
export const validatePincodeForCity = (state, city, pincode) => {
  if (!state || !city || !pincode) return false;
  const pincodes = getPincodesForCity(state, city);
  return pincodes.includes(pincode);
};

// Function to get state and city for a given pincode
export const getLocationForPincode = (pincode) => {
  if (!pincode) return null;
  
  for (const state of Object.keys(indianCities)) {
    for (const city of Object.keys(indianCities[state])) {
      if (indianCities[state][city].pincodes.includes(pincode)) {
        return {
          state: state,
          city: city,
          districts: indianCities[state][city].districts
        };
      }
    }
  }
  return null;
};

// Function to search cities by partial name
export const searchCities = (searchTerm, state = null) => {
  if (!searchTerm) return [];
  
  const results = [];
  const searchLower = searchTerm.toLowerCase();
  
  const statesToSearch = state ? [state] : Object.keys(indianCities);
  
  statesToSearch.forEach(stateName => {
    if (indianCities[stateName]) {
      Object.keys(indianCities[stateName]).forEach(city => {
        if (city.toLowerCase().includes(searchLower)) {
          results.push({
            city: city,
            state: stateName,
            districts: indianCities[stateName][city].districts
          });
        }
      });
    }
  });
  
  return results;
};

// Function to search pincodes by partial match
export const searchPincodes = (searchTerm, state = null, city = null) => {
  if (!searchTerm) return [];
  
  const results = [];
  
  if (state && city && indianCities[state] && indianCities[state][city]) {
    // Search within specific state and city
    return indianCities[state][city].pincodes.filter(pincode => 
      pincode.startsWith(searchTerm)
    );
  } else if (state && indianCities[state]) {
    // Search within specific state
    Object.keys(indianCities[state]).forEach(cityName => {
      indianCities[state][cityName].pincodes.forEach(pincode => {
        if (pincode.startsWith(searchTerm)) {
          results.push({
            pincode: pincode,
            city: cityName,
            state: state
          });
        }
      });
    });
  } else {
    // Search all pincodes
    Object.keys(indianCities).forEach(stateName => {
      Object.keys(indianCities[stateName]).forEach(cityName => {
        indianCities[stateName][cityName].pincodes.forEach(pincode => {
          if (pincode.startsWith(searchTerm)) {
            results.push({
              pincode: pincode,
              city: cityName,
              state: stateName
            });
          }
        });
      });
    });
  }
  
  return results;
};

// Function to validate complete address
export const validateCompleteAddress = (state, city, district, pincode) => {
  const errors = {};
  
  if (!state) {
    errors.state = 'State is required';
  }
  
  if (!city) {
    errors.city = 'City is required';
  } else if (state && !validateCityForState(state, city)) {
    errors.city = `${city} is not a valid city in ${state}`;
  }
  
  if (!district) {
    errors.district = 'District is required';
  } else if (state && city && !validateDistrictForCity(state, city, district)) {
    errors.district = `${district} is not a valid district for ${city}, ${state}`;
  }
  
  if (!pincode) {
    errors.pincode = 'Pincode is required';
  } else if (pincode.length !== 6) {
    errors.pincode = 'Pincode must be exactly 6 digits';
  } else if (!/^\d{6}$/.test(pincode)) {
    errors.pincode = 'Pincode must contain only numbers';
  } else if (state && city && !validatePincodeForCity(state, city, pincode)) {
    errors.pincode = `${pincode} is not a valid pincode for ${city}, ${state}`;
  }
  
  return {
    isValid: Object.keys(errors).length === 0,
    errors: errors
  };
};

